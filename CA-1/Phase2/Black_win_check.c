#include "Black_win_check.h"
int Black_win_check(char GB[600][600])
{
	int ans = 0;
	//horizontal
	for (int i = 50; i < 600; i += 100)
	{
		for (int j = 50; j < 450; j += 100)
		{
			if (GB[i][j] == 'b' && GB[i][j + 100] == 'b' && GB[i][j + 200] == 'b'
				&& GB[i][j + 300] == 'b')
			{
				if (i == 50)
				{
					if (GB[150][j] == 'b' || GB[150][j + 300] == 'b')
					{
						ans = 1;
					}
				}
				else if (i == 550)
				{
					if (GB[450][j] == 'b' || GB[450][j + 300] == 'b')
					{
						ans = 1;
					}
				}
				else
				{
					if (GB[i - 100][j] == 'b' || GB[i + 100][j] == 'b'
						|| GB[i - 100][j + 300] == 'b' || GB[i + 100][j - 300] == 'b')
					{
						ans = 1;
					}
				}
			}
		}
	}
	//vertical
	for (int k = 50; k < 600; k += 100)
	{
		for (int t = 50; t < 450; t += 100)
		{
			if (GB[t][k] == 'b' && GB[t + 100][k] == 'b' && GB[t + 200][k] == 'b'
				&& GB[t + 300][k] == 'b')
			{
				if (k == 50)
				{
					if (GB[t][150] == 'b' || GB[t + 300][1] == 'b')
					{
						ans = 1;
					}
				}
				if (k == 550)
				{
					if (GB[t][400] == 'b' || GB[t + 300][400] == 'b')
					{
						ans = 1;
					}
				}
				else
				{
					if (GB[t][k - 100] == 'b' || GB[t + 300][k - 100] == 'b'
						|| GB[t][k + 100] == 'b' || GB[t + 300][k + 100] == 'b')
					{
						ans = 1;
					}
				}
			}
		}
	}
	//Diagonal(sub diameter)
	for (int z = 450; z < 600; z += 100)
	{
		for (int y = 50; y < 450; y += 100)
		{
			if (GB[z][y] == 'b' && GB[z - 100][y + 100] == 'b'
				&& GB[z - 200][y + 200] == 'b' && GB[z - 300][y + 300] == 'b')
			{
				if (z == 350 && y == 50)
				{
					if (GB[450][150] == 'b' || GB[150][450] == 'b')
					{
						ans = 1;
					}
				}
				else if (z == 350 && y == 150)
				{
					if (GB[150][550] == 'b' || GB[450][250] == 'b' || GB[250][50] == 'b')
					{
						ans = 1;
					}
				}
				else if (z == 350 && y == 250)
				{
					if (GB[450][350] == 'b' || GB[250][150] == 'b')
					{
						ans = 1;
					}
				}
				else if (z == 450 && y == 50)
				{
					if (GB[550][150] == 'b' || GB[50][250] == 'b' || GB[250][450] == 'b')
					{
						ans = 1;
					}
				}
				else if (z == 450 && y == 150)
				{
					if (GB[50][350] == 'b' || GB[250][550] == 'b' || GB[350][50] == 'b'
						|| GB[550][250] == 'b')
					{
						ans = 1;
					}
				}
				else if (z == 450 && y == 250)
				{
					if (GB[50][450] == 'b' || GB[550][350] == 'b' || GB[350][150] == 'b')
					{
						ans = 1;
					}
				}
				else if (z == 550 && y == 50)
				{
					if (GB[150][250] == 'b' || GB[350][450] == 'b')
					{
						ans = 1;
					}
				}
				else if (z == 550 && y == 150)
				{
					if (GB[450][50] == 'b' || GB[150][350] == 'b' || GB[350][550] == 'b')
					{
						ans = 1;
					}
				}
				else
				{
					if (GB[450][150] == 'b' || GB[150][450] == 'b')
					{
						ans = 1;
					}
				}
			}
		}
	}
	//Diagonal(main diameter)
	for (int v = 350; v < 600; v += 100)
	{
		for (int p = 550; p > 250; p -= 100)
		{
			if (GB[v][p] == 'b' && GB[v - 100][p - 100] == 'b'
				&& GB[v - 200][p - 200] == 'b' && GB[v - 300][p - 300] == 'b')
			{
				if (v == 350 && p == 550)
				{
					if (GB[450][450] == 'b' || GB[150][150] == 'b')
					{
						ans = 1;
					}
				}
				else if (v == 350 && p == 450)
				{
					if (GB[250][550] == 'b' || GB[450][350] == 'b' || GB[150][50] == 'b')
					{
						ans = 1;
					}
				}
				else if (v == 350 && p == 350)
				{
					if (GB[250][450] == 'b' || GB[450][250] == 'b')
					{
						ans = 1;
					}
				}
				else if (v == 450 && p == 550)
				{
					if (GB[550][450] == 'b' || GB[50][350] == 'b' || GB[250][150] == 'b')
					{
						ans = 1;
					}
				}
				else if (v == 450 && p == 450)
				{
					if (GB[550][350] == 'b' || GB[350][550] == 'b' || GB[50][250] == 'b'
						|| GB[250][50] == 'b')
					{
						ans = 1;
					}
				}
				else if (v == 450 && p == 350)
				{
					if (GB[350][450] == 'b' || GB[550][250] == 'b' || GB[50][150] == 'b')
					{
						ans = 1;
					}
				}
				else if (v == 550 && p == 550)
				{
					if (GB[350][150] == 'b' || GB[150][350] == 'b')
					{
						ans = 1;
					}
				}
				else if (v == 550 && p == 450)
				{
					if (GB[450][550] == 'b' || GB[350][50] == 'b' || GB[150][250] == 'b')
					{
						ans = 1;
					}
				}
				else
				{
					if (GB[450][450] == 'b' || GB[150][150] == 'b')
					{
						ans = 1;
					}
				}
			}
		}
	}
	return ans;
}