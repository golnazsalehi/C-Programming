int White_win_check(char GB[600][600])
{
	int temp = 0;
	//horizontal
	for (int a = 50; a < 600; a += 100)
	{
		for (int b = 50; b < 450; b += 100)
		{
			if (GB[a][b] == 'w' && GB[a][b + 100] == 'w' && GB[a][b + 200] == 'w'
				&& GB[a][b + 300] == 'w')
			{
				if (a == 0)
				{
					if (GB[150][b] == 'w' || GB[150][b + 300] == 'w')
					{
						temp = 1;
					}
				}
				else if (a == 550)
				{
					if (GB[450][b] == 'w' || GB[450][b + 300] == 'w')
					{
						temp = 1;
					}
				}
				else
				{
					if (GB[a - 100][b] == 'w' || GB[a + 100][b] == 'w'
						|| GB[a - 100][b + 300] == 'w' || GB[a + 100][b - 300] == 'w')
					{
						temp = 1;
					}
				}
			}
		}
	}
	//vertical
	for (int c = 50; c < 600; c += 100)
	{
		for (int d = 50; d < 450; d += 100)
		{
			if (GB[d][c] == 'w' && GB[d + 100][c] == 'w' && GB[d + 200][c] == 'w'
				&& GB[d + 300][c] == 'w')
			{
				if (c == 50)
				{
					if (GB[d][150] == 'w' || GB[d + 300][150] == 'w')
					{
						temp = 1;
					}
				}
				if (c == 550)
				{
					if (GB[d][450] == 'w' || GB[d + 300][450] == 'w')
					{
						temp = 1;
					}
				}
				else
				{
					if (GB[d][c - 100] == 'w' || GB[d + 300][c - 100] == 'w'
						|| GB[d][c + 100] == 'w' || GB[d + 300][c + 100] == 'w')
					{
						temp = 1;
					}
				}
			}
		}
	}
	//Diagonal(sub diameter)
	for (int e = 350; e < 600; e += 100)
	{
		for (int f = 50; f < 350; f += 100)
		{
			if (GB[e][f] == 'w' && GB[e - 100][f + 100] == 'w'
				&& GB[e - 200][f + 200] == 'w' && GB[e - 300][f + 300] == 'w')
			{
				if (e == 450 && f == 50)
				{
					if (GB[450][150] == 'w' || GB[150][450] == 'w')
					{
						temp = 1;
					}
				}
				else if (e == 350 && f == 150)
				{
					if (GB[150][550] == 'w' || GB[450][250] == 'w' || GB[250][50] == 'w')
					{
						temp = 1;
					}
				}
				else if (e == 350 && f == 250)
				{
					if (GB[450][350] == 'w' || GB[250][150] == 'w')
					{
						temp = 1;
					}
				}
				else if (e == 450 && f == 50)
				{
					if (GB[550][150] == 'w' || GB[50][250] == 'w' || GB[250][450] == 'w')
					{
						temp = 1;
					}
				}
				else if (e == 450 && f == 150)
				{
					if (GB[50][350] == 'w' || GB[250][50] == 'w' || GB[350][50] == 'w'
						|| GB[550][250] == 'w')
					{
						temp = 1;
					}
				}
				else if (e == 450 && f == 250)
				{
					if (GB[50][450] == 'w' || GB[550][350] == 'w' || GB[350][150] == 'w')
					{
						temp = 1;
					}
				}
				else if (e == 550 && f == 50)
				{
					if (GB[150][250] == 'w' || GB[350][450] == 'w')
					{
						temp = 1;
					}
				}
				else if (e == 550 && f == 150)
				{
					if (GB[450][50] == 'w' || GB[150][350] == 'w' || GB[350][550] == 'w')
					{
						temp = 1;
					}
				}
				else
				{
					if (GB[450][150] == 'w' || GB[150][450] == 'w')
					{
						temp = 1;
					}
				}
			}
		}
	}
	//Diagonal(main diameter)
	for (int s = 350; s < 600; s += 100)
	{
		for (int u = 550; u > 250; u -= 100)
		{
			if (GB[s][u] == 'w' && GB[s - 100][u - 100] == 'w'
				&& GB[s - 200][u - 200] == 'w' && GB[s - 300][u - 300] == 'w')
			{
				if (s == 350 && u == 550)
				{
					if (GB[450][450] == 'w' || GB[150][150] == 'w')
					{
						temp = 1;
					}
				}
				else if (s == 350 && u == 450)
				{
					if (GB[250][550] == 'w' || GB[450][350] == 'w' || GB[150][50] == 'w')
					{
						temp = 1;
					}
				}
				else if (s == 350 && u == 350)
				{
					if (GB[250][450] == 'w' || GB[450][250] == 'w')
					{
						temp = 1;
					}
				}
				else if (s == 450 && u == 550)
				{
					if (GB[550][450] == 'w' || GB[50][350] == 'w' || GB[250][150] == 'w')
					{
						temp = 1;
					}
				}
				else if (s == 450 && u == 450)
				{
					if (GB[550][350] == 'w' || GB[350][550] == 'w' || GB[50][250] == 'w'
						|| GB[250][50] == 'w')
					{
						temp = 1;
					}
				}
				else if (s == 450 && u == 350)
				{
					if (GB[350][450] == 'w' || GB[550][250] == 'w' || GB[50][150] == 'w')
					{
						temp = 1;
					}
				}
				else if (s == 550 && u == 550)
				{
					if (GB[350][150] == 'w' || GB[150][350] == 'w')
					{
						temp = 1;
					}
				}
				else if (s == 550 && u == 450)
				{
					if (GB[450][550] == 'w' || GB[350][50] == 'w' || GB[150][250] == 'w')
					{
						temp = 1;
					}
				}
				else
				{
					if (GB[450][450] == 'w' || GB[150][150] == 'w')
					{
						temp = 1;
					}
				}
			}
		}
	}
	return temp;
}